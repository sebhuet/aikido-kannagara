                <h2>Qu'est-ce que l'Aïkido ?</h2>
                <p>
                    L'Aïkido ne peut être classé, à proprement parler, parmi les sports car il <strong>exclut toute idée de compétition</strong>.
                    C'est un art martial qui vise à développer l'harmonie entre le corps et l'esprit, et avec les autres.
                </p>
                <p>
                    Les techniques d'aïkido reposent sur <strong>l'utilisation de la force de l'adversaire</strong>
                    et ne nécessitent pas d'aptitudes physiques particulières. Le travail musculaire s'effectue
                    naturellement en accord avec la constitution de chacun.
                </p>
                <p>
                    C'est pourquoi l'aïkido est pratiqué quotidiennement aussi bien par des <strong>femmes, des hommes,
                    des enfants</strong> que des <strong>personnes plus âgées</strong>.
                </p>

                <h2>Histoire de l'Aïkido</h2>
                <p>
                    L'Aïkido est né au début du siècle sous l'impulsion de <strong>Maître Morihei Ueshiba</strong> (1883-1969),
                    dont le petit-fils (Moriteru Ueshiba, actuel Doshu) assure aujourd'hui la succession à la tête de l'Aïkikaï de Tokyo.
                </p>

                <div class="info-box">
                    <h4 class="info-box__title">Morihei Ueshiba, le Fondateur</h4>
                    <p>
                        Surnommé O Sensei ("Grand Maître"), Morihei Ueshiba a créé l'aïkido en synthétisant
                        plusieurs arts martiaux traditionnels japonais, notamment le Daito-ryu Aiki-jujutsu.
                        Sa vision était de créer un art qui permettrait de vaincre un adversaire sans le blesser,
                        transformant ainsi le conflit en harmonie.
                    </p>
                </div>

                <h2>L'Aïkido en France</h2>
                <p>
                    L'aïkido a été introduit en France par <strong>Maîtres Minoru Mochizuki et Tadashi Abe</strong>
                    au début des années 1950, ainsi que par Maîtres Noro et Nakazono.
                </p>
                <p>
                    <strong>Maître André Nocquet</strong> (1914-1999) fut le premier Français à se rendre au Japon
                    pour étudier l'aïkido auprès du Fondateur. Il fut uchi-deshi (élève interne) de Maître Ueshiba.
                </p>
                <p>
                    <strong>Maître Nobuyoshi Tamura</strong> (1933-2010), l'un des élèves directs du Fondateur,
                    a vécu plus de 30 ans en France où il a diffusé, en tant que délégué européen,
                    un aïkido fidèle à l'enseignement qu'il a reçu. Son influence sur l'aïkido français
                    est considérable et perdure aujourd'hui à travers ses nombreux élèves.
                </p>

                <div class="info-box">
                    <h4 class="info-box__title">Notre filiation</h4>
                    <p>
                        Notre club s'inscrit dans la lignée de l'enseignement de Maître Tamura,
                        transmis par ses élèves directs. Jean-Marc Chamot a notamment pratiqué
                        avec André Nocquet, premier uchi-deshi français du Fondateur.
                    </p>
                </div>

                <h2>Principes fondamentaux</h2>
                <p>
                    L'aïkido repose sur plusieurs principes essentiels :
                </p>
                <ul>
                    <li><strong>Ai</strong> (合) : Harmonie, union</li>
                    <li><strong>Ki</strong> (気) : Énergie vitale</li>
                    <li><strong>Do</strong> (道) : La voie, le chemin</li>
                </ul>
                <p>
                    Ainsi, l'Aïkido peut se traduire par "la voie de l'harmonie des énergies".
                </p>

                <h2>La pratique</h2>
                <p>
                    L'aïkido se pratique en <strong>keikogi</strong> (tenue d'entraînement blanche)
                    et souvent en <strong>hakama</strong> (pantalon large traditionnel) pour les pratiquants avancés.
                </p>
                <p>
                    Les techniques comprennent :
                </p>
                <ul>
                    <li><strong>Tachi-waza</strong> : Techniques debout</li>
                    <li><strong>Suwari-waza</strong> : Techniques à genoux</li>
                    <li><strong>Hanmi-handachi</strong> : Un partenaire debout, l'autre à genoux</li>
                    <li><strong>Buki-waza</strong> : Techniques avec armes (jo, bokken, tanto)</li>
                </ul>

