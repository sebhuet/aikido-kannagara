    <script>
        // FAQ Accordion
        document.addEventListener('DOMContentLoaded', function() {
            const faqItems = document.querySelectorAll('.faq-item');
            const searchInput = document.getElementById('faqSearchInput');
            const searchResults = document.getElementById('faqSearchResults');
            const categoryButtons = document.querySelectorAll('.faq-category-btn');
            const sections = document.querySelectorAll('.faq-section');

            // Gestion de l'accordéon
            faqItems.forEach(item => {
                const question = item.querySelector('.faq-question');

                question.addEventListener('click', function() {
                    const isActive = item.classList.contains('active');

                    // Fermer tous les autres
                    faqItems.forEach(otherItem => {
                        otherItem.classList.remove('active');
                        otherItem.querySelector('.faq-question').setAttribute('aria-expanded', 'false');
                    });

                    // Basculer l'élément actuel
                    if (!isActive) {
                        item.classList.add('active');
                        this.setAttribute('aria-expanded', 'true');
                    }
                });
            });

            // Fonction de recherche
            function searchFAQ(query) {
                query = query.toLowerCase().trim();
                let visibleCount = 0;
                let totalCount = faqItems.length;

                if (query === '') {
                    // Réinitialiser l'affichage
                    faqItems.forEach(item => {
                        item.style.display = '';
                    });
                    sections.forEach(section => {
                        section.style.display = '';
                    });
                    searchResults.textContent = '';
                    return;
                }

                faqItems.forEach(item => {
                    const questionText = item.querySelector('.faq-question').textContent.toLowerCase();
                    const answerText = item.querySelector('.faq-answer__content').textContent.toLowerCase();

                    if (questionText.includes(query) || answerText.includes(query)) {
                        item.style.display = '';
                        visibleCount++;
                    } else {
                        item.style.display = 'none';
                    }
                });

                // Masquer les sections vides
                sections.forEach(section => {
                    const visibleItems = section.querySelectorAll('.faq-item[style=""]');
                    section.style.display = visibleItems.length > 0 ? '' : 'none';
                });

                // Afficher le résultat de la recherche
                if (visibleCount === 0) {
                    searchResults.textContent = 'Aucune question trouvée';
                } else if (visibleCount === 1) {
                    searchResults.textContent = '1 question trouvée';
                } else {
                    searchResults.textContent = `${visibleCount} questions trouvées`;
                }
            }

            // Écouteur sur la recherche
            searchInput.addEventListener('input', function(e) {
                searchFAQ(e.target.value);
            });

            // Gestion des filtres par catégorie
            categoryButtons.forEach(button => {
                button.addEventListener('click', function() {
                    const category = this.dataset.category;

                    // Retirer la classe active de tous les boutons
                    categoryButtons.forEach(btn => btn.classList.remove('active'));
                    
                    // Ajouter la classe active au bouton cliqué
                    this.classList.add('active');

                    // Réinitialiser la recherche
                    searchInput.value = '';
                    searchResults.textContent = '';
                    faqItems.forEach(item => {
                        item.style.display = '';
                    });

                    // Filtrer les sections
                    if (category === 'all') {
                        sections.forEach(section => {
                            section.classList.remove('hidden');
                        });
                    } else {
                        sections.forEach(section => {
                            if (section.dataset.category === category) {
                                section.classList.remove('hidden');
                            } else {
                                section.classList.add('hidden');
                            }
                        });
                    }
                });
            });
        });
    </script>
